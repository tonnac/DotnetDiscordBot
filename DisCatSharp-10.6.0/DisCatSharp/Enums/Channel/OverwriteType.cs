namespace DisCatSharp.Enums;

/// <summary>
/// Represents a channel permission overwrite's type.
/// </summary>
public enum OverwriteType
{
	/// <summary>
	/// Specifies that this overwrite applies to a role.
	/// </summary>
	Role = 0,

	/// <summary>
	/// Specifies that this overwrite applies to a member.
	/// </summary>
	Member = 1
}
